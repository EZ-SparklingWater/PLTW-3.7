public class Cereal {
    private String brand;
    private double rating;

    public Cereal (String x, double y){
        brand = x;
        rating = y;
    }


   








    public String getBrand(){
        return brand;
    }
    public double getRating(){
        return rating;
    }
    public void setBrand(String x){
        brand = x;
    }
    public void setRating(double y){
        rating = y;
    }

}